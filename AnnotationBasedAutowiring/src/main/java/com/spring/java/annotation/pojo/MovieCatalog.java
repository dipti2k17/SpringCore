package com.spring.java.annotation.pojo;

public class MovieCatalog {
	private String actionVhsCatalog;
	private String comedyVhsCatalog;
	private String actionDvdCatalog;
	private String comedyBluRayCatalog;
	
	public MovieCatalog() { };
	
	public String getActionVhsCatalog() {
		return actionVhsCatalog;
	}
	public void setActionVhsCatalog(String actionVhsCatalog) {
		this.actionVhsCatalog = actionVhsCatalog;
	}
	public String getComedyVhsCatalog() {
		return comedyVhsCatalog;
	}
	public void setComedyVhsCatalog(String comedyVhsCatalog) {
		this.comedyVhsCatalog = comedyVhsCatalog;
	}
	public String getActionDvdCatalog() {
		return actionDvdCatalog;
	}
	public void setActionDvdCatalog(String actionDvdCatalog) {
		this.actionDvdCatalog = actionDvdCatalog;
	}
	public String getComedyBluRayCatalog() {
		return comedyBluRayCatalog;
	}
	public void setComedyBluRayCatalog(String comedyBluRayCatalog) {
		this.comedyBluRayCatalog = comedyBluRayCatalog;
	}
	
	

}
